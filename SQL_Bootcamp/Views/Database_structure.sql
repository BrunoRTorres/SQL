Database server --? Stores, manages, and provides access to databases
                --? for users or applications.

Database --? Collection of information that is stored in a structured way.

Schema --? Logical layers that groups related objects together.

Table --? A place where data is stored and organized into rows and columns.

View --? Is a virtual table that shows data without storing it physically.
     --? Are persisted SQL queries in the database.

DDL -Data Definition Language --? A set of commands that allows us to define
                              --? and manage the structure of a database.
